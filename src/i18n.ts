export function t(key: keyof Translation, params: Record<string, string> = {}): string {
    const lang = vscode.workspace.getConfiguration('antigravityTelegramControl').get<string>('language') ?? 'en';
    const tranSet = translations[lang] || translations['en'];
    let text = tranSet[key] || translations['en'][key] || '';

    for (const [pk, pv] of Object.entries(params)) {
        text = text.replace(`{${pk}}`, pv);
    }
    return text;
}
